# operaciones matematicas basicas
# suma
print('suma')
print('ingrese el primer valor a sumar')
save_num1 = float (input())
print('ingrese el segundo valor a sumar')
save_num2 = float (input())
result = save_num1 + save_num2
print('El resultado de la suma de ',save_num1,' + ',save_num2,' = ',result)
# resta
print('RESTA')
save_value1 = float(input('ingrese el primer valor a restar'))
save_value2 = float(input('ingrese el segundo valor a restar'))
result = save_value1 - save_value2
print('El resultado de la resta de ',save_value1,' - ',save_value2,' = ',result)
# multiplicacion
print('MULTIPLICACION')
save_x1 = float(input('ingrese el primer valor a multiplicar \n'))
save_x2 = float(input('ingrese el segundo valor a multiplicar \n'))
result = save_x1 * save_x2
print('El resultado de la multiplicacion de ',save_x1,' x ',save_x2,' = ',result)
# division
print('DIVISION')
save_g1 = float(input('ingrese el primer valor a dividir \n'))
save_g2 = float(input('ingrese el segundo valor a dividir \n'))
result = save_g1 / save_g2
print('El resultado de la division de ',save_g1,' / ',save_g2,' = ',result)