#VARIABLES
#VARIABLES ENTERAS
number_enteros = 24
print(type(number_enteros))
print('Este es un numero entero: ', number_enteros)
#VARIABLES FLOTANTES O DECIMALES
number_decimales = 6.17
print(type(number_decimales))
print('Este es un numero flotante: ', number_decimales)
#VARIABLES BOOLEANAS
value_verdadero = True
value_falso = False
print(type(value_verdadero))
print(type(value_falso))
print('Esta es una variable booleana verdadera: ', value_verdadero)
print('Esta es una variable booleana falsa: ', value_falso)
#VARIABLES STRING O CADENA DE CARACTERES
nombres = 'Francisco Javier'
print(type(nombres))
print('Esta es una cadena de caracteres: ', nombres)